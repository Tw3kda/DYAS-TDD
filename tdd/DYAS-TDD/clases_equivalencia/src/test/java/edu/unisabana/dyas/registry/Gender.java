package edu.unisabana.dyas.registry;

/**
 * Enum que representa el género de una persona. Puede ser uno de los tres.
 * valores: masculino, femenino o no identificado.
 */
public enum Gender {
    /**
     * Género masculino.
     */
    MALE,
    /**
     * Género femenino.
     */
    FEMALE,
    /**
     * Género no identificado.
     */
    UNIDENTIFIED;

}
